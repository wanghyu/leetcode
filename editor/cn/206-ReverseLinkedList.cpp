int main(){
    [[maybe_unused]] Solution s;

    ListNode l5(5, nullptr);
    ListNode l4(4, &l5);
    ListNode l3(3, &l4);
    ListNode l2(2, &l3);
    ListNode l1(1, &l2);

    auto head = s.reverseList(&l1);
    printList(head);
    return 0;
}